#include "../libft.h"

char	ft_itoa_char_hexa(int nbr)
{
	char	c;

	if (nbr == 0)
		c = '0';
	while (nbr > 0 && nbr <= 9)
	{
		c = (nbr % 10) + '0';
		nbr = nbr / 16;
	}
	return (c);
}
